  CREATE OR REPLACE EDITIONABLE FUNCTION "CENTRO_AYUDA"."CA_BUSCA_PROYECTO_USUARIO" (p_usuario in varchar2, p_app_id in number) return varchar2 is
  v_proyecto varchar2(200);
begin
  begin
      SELECT py.nombre 
             INTO v_proyecto 
      FROM usuario_suc us
      INNER JOIN personal_sanatorio ps ON (us.id_personal_sanatorio = ps.id_personal)
      INNER JOIN ca_proyecto_principal pp ON (ps.id_personal = pp.personal_id) 
      INNER JOIN ca_proyecto py ON (pp.proyecto_id = py.id_proyecto)
      WHERE us.perfil_usuario = p_usuario
      AND pp.app_id = p_app_id;      
  exception
    when others then
      null;
  end;
  return v_proyecto;
end CA_BUSCA_PROYECTO_USUARIO;

